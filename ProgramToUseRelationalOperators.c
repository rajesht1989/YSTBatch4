// Requirement:write a program to use an Relational operators
#include <stdio.h>
int main(){
    int b = 19;
    int p = 5;
    printf("b == p %d\n", b==p);
    printf("b > p %d\n", b>p);
    printf("b >= p %d\n", b>=p);
    printf("b <= p %d\n", b<=p);
    printf("b < p %d\n", b<p);
    printf("b != p %d\n", b!=p);
    return 0;
}
